# Preconditions:
#   1st parameter (a0) address of the first element of the dimension info (address of asize[0])
#   2nd parameter (a1) address of the first element of the frame array (address of frame[0][0])
#   3rd parameter (a2) address of the first element of the window array (address of window[0][0])
# Postconditions:	
#   result (v0) x coordinate of the block in the frame with the minimum SAD
#          (v1) y coordinate of the block in the frame with the minimum SAD


# Begin subroutine
vbsme:  
    li      $v0, 0                        # reset $v0 and $V1
    li      $v1, 0

   # insert your code here
   addi     $sp, $sp, -4                  # Make space on stack
   sw       $ra, 0($sp)                   # Save return address
   addi     $s0, $zero, 0                 # initializing
   addi     $s1, $zero, 32767             # SuperSad = largest 32 bit signed number
   lw       $s2, 0($a0)                   # load frame x dimention to s2 e.g. x1
   lw       $s3, 4($a0)                   # load frame y dimention to s3 e.g. y1
   lw       $s4, 8($a0)                   # load window x dimention to s4 e.g. x2
   lw       $s5, 12($a0)                  # load window y dimention to s5 e.g. y2
   sub      $s6, $s2, $s4                 # xDiff
   sub      $s7, $s3, $s5                 # yDiff
   addi     $t0, $zero, 0                 # i = 0
   addi     $t1, $zero, 0                 # j = 0
   addi     $t2, $zero, 0                 # k = 0
   addi     $t3, $zero, 0                 # l = 0
   jal      SAD                           # calculating the initial SAD

   move     $s1, $s0                      # SupserSAD = SAD
   move     $v0, $t1                      # v0 =  j
   move     $v1, $t0                      # v1 = i
   sub      $t4, $t0, $s6                 # will be zero if already completed y direction
   sub      $t2, $t1, $s7 			      # will be zero if already completed x direction
   or 		$t4, $t4, $t2 				  # setting $t4 = 0 if $t4 = 0 and $t2 = 0
   beq 		$t4, $zero, done

main_loop:                                # while  i < xDiff or j < yDiff:
   beq      $t0, $zero, top_or_right      # if at the top (i == 0)
   beq      $t1, $s7, top_or_right        # if at the right (j == yDiff)
   beq      $t1, $zero, bottom_or_left    # if at left (j == 0)
   beq      $t0, $s6, bottom_or_left      # i == xDIff
   beq      $t7, $zero, middle_going_up   # if diagonally going up in the frame
   addi     $t0, $t0, 1                   # if diagonally going down in the frame
   addi     $t1, $t1, -1
   j        done_branching
top_or_right:
   beq      $t7, $zero, window_going_up   # if the direction was previously upwards
   addi     $t0, $t0, 1                   # if on the top or right and going to move diagonally down
   addi     $t1, $t1, -1                  
   j done_branching
window_going_up:
   addi     $t7, $t7, 1                   # setting the direction to 1 because will change direction
   beq      $t1, $s7, window_at_right     # if on the right
   addi     $t1, $t1, 1                   # if on the top and going to move right
   j        done_branching
window_at_right:
   addi     $t0, $t0, 1                   # moving down
   j        done_branching
bottom_or_left:
   bne      $t7, $zero, window_going_down # if direction == 1
   addi     $t1, $t1, 1                   # if on bottom or left and going to move diagonally up
   addi     $t0, $t0, -1
   j done_branching
window_going_down:
   addi     $t7, $t7, -1                  # setting direction to zero because will change direction
   beq      $t0, $s6, window_on_bottom    # if on the bottom
   addi     $t0, $t0, 1                   # if on the left and going to move down
   j        done_branching
window_on_bottom:
   addi     $t1, $t1, 1                   # moving to the right
   j        done_branching
middle_going_up:
   addi     $t1, $t1, 1                   # will move diagonally up without changing direction
   addi     $t0, $t0, -1 # i--
done_branching:
   addi     $s0, $zero, 0                 # Setting SAD back to 0 for new SAD calculation
   jal      SAD                           # calculating new SAD
   beq      $s1, $s0, equal_to            # if SAD == SuperSAD, calculate new coordiantes
   slt      $t9, $s0, $s1                 # if SAD < SuperSAD, calculate new SuperSAD and coordinates
   beq      $t9, $zero, no_new_SAD
equal_to:   
   move     $s1, $s0                      # SupserSAD = SAD
   move     $v0, $t0                      # v0 =  j
   move     $v1, $t1                      # v1 = i
no_new_SAD:
   slt      $t9, $t0, $s6                 # if i < xDiff go back to the loop
   bne      $t9, $zero, main_loop
   slt      $t9, $t1, $s7                 # if j < yDiff go back to the loop
   bne      $t9, $zero, main_loop
done:
   lw       $ra, 0($sp)                   # getting the return address from the stack
   addi     $sp, $sp, 4                   # clearing the stack   
   jr       $ra


SAD:                                      # SAD = SAD + abs((frame[((i+k)*y1)+(j+l)] - window[(k*y2)+l]));
   addi     $t2, $zero, 0                 # k = 0 at the beggining of the first loop 
first_loop:
   addi     $t3, $zero, 0                 # l = 0 at the beggining of the second loop
second_loop:
   add      $t4, $t0, $t2                 # add i and k together
   mul      $t4, $t4, $s3                 # multiply i+k by the dimention of the frame
   add      $t4, $t4, $t1                 # add (i+k)*y1 to j
   add      $t4, $t4, $t3                 # add (i+k)*y1+j to l
   sll      $t4, $t4, 2                   # byte addressing for frame
   add      $t4, $t4, $a1                 # add to the address of the first element of frame array
   lw       $t4, 0($t4)                   # load element [i+k][j+l] to t4
   mul      $t5, $s5, $t2                 # multiply k and y2; the dimention of the window
   add      $t5, $t5, $t3                 # add (k*y2) to l
   sll      $t5, $t5, 2                   # byte addressing for window
   add      $t5, $t5, $a2                 # add to the address of the first element of the window array
   lw       $t5, 0($t5)                   # load element [k][l] to t5
   sub      $t4, $t4, $t5                 # subtract window[k][l] from frame[i+k][j+l]
   slt      $t5, $t4, $zero               # if the difference is less than zero set to 1
   beq      $t5, $zero, SKIP              # if positive skip
   sub      $t4, $zero, $t4               # if negative switch signs
SKIP:
   add      $s0, $s0, $t4                 # SAD = SAD + $t4
   addi     $t3, $t3, 1                   # l++
   slt      $t4, $t3, $s5                 # $t4 = (l < y2)
   bne      $t4, $zero, second_loop       # if l is not less than y2 go through inner loop again
   addi     $t2, $t2, 1                   # k++
   slt      $t4, $t2, $s4                 # $t4 = (k < x2)
   bne      $t4, $zero, first_loop        # if k is not less x2 go through outer loop again
   jr       $ra                           # jumping back $ra
